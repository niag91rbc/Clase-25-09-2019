
package java001;

public class Java001 {

    public static void main(String[] args) {
        // variables: son espacio de memoria
        int a;
        float b;
        
        // operadoradores aritméticos
        // (+) adición o suma
        // (-) sustracción o resta
        // (*) producto o multiplicación
        // (/) couciente o división
        
        int r1;
        float r2;
        
        a=13;
        b=5.2f;
        
        // fin de la entrada de datos
        
        // proceso obtener el producto de tipo int, float        
        r1=(int)(a*b); 
        r2=a*b;
        
        // Resultados o salida
        System.out.println("Resultado 1 : "+r1);
        System.out.println("Resultado 2 : "+r2);
        
    }
    
}
